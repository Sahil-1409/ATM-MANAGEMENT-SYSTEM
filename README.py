# # 🏦 ATM Management System (Python)

# ## 📌 Description
# This is a simple ATM Management System built using Python. It simulates real ATM operations like account creation, balance inquiry, deposit, withdrawal, and updating account details.

# ---

# ## 🚀 Features
# - 🔐 PIN Authentication
# - 💰 Check Balance
# - 💲 Deposit Money
# - 💳 Withdraw Money
# - 🔄 Update Account Details
# - ✅ Input Validation

# ---

# ## 🛠️ Technologies Used
# - Python 3
# - CLI (Command Line Interface)
# - Dictionary (for data storage)

# ---

# ## 📂 Project Structure

# ATM Management System/
# │── main.py                  # Main program (menu)
# │── Pin_authentication.py    # Account creation & PIN setup
# │── Check_balance.py         # Balance checking
# │── Deposit.py               # Deposit functionality
# │── Withdraw.py              # Withdraw functionality
# │── update_acc_details.py    # Update account info
# │── Utils.py                 # Stores data (accounts, balance)

